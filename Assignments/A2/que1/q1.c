#include<stdio.h>
int main(){
    int n=10;
    printf("Before Post increment value of n is : %d\n", n);
    n++;
    printf("After Post increment value of n is : %d\n", n);
    int m = 12;
    printf("Before Pre increment value of m is : %d\n", m);
    ++m;
    printf("After Pre increment value of n is : %d\n", m);
}